// C++ Multiplication of Two Numbers

#include<iostream>

using namespace std;

int main()
{
     int multiplicand, multiplier, product;
     
     cout << "Enter the multiplicand: ";
     cin >> multiplicand;
     
     cout << "Enter the multiplier: ";
     cin >> multiplier;
     
     product = multiplicand*multiplier;
     
     cout << "The product is: "<<product<<"";
     
     return 0;
}
